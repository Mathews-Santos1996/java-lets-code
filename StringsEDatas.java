public class StringsEDatas {

    public static void main (String [] args) {
        // Olá, {nome}, Hoje é {dia-da-semana}, BOM DIA 

        String nome = "Mathews";
        System.out.println(nome.toUpperCase()); //toUpperCase -> transforma todas as letras em maiúsculas
        System.out.println(nome.toLowerCase()); //toLowerCase -> transforma todas as letras em minúsculas
        System.out.println(nome.length()); // tamanho da string

        String nomeOutro = "mathews";
        System.out.println(nome.equals(nomeOutro)); // equals -> faz uma comparação entre as strings, depois retorna true ou false
        





    }
    
}
